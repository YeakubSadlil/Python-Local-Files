def binarySearch(a,b):
    low = 0
    high = min(a,b)

    while(low <= high):
        mid = low + (high-low)//2
        if (a >= mid and b >= mid and a+b >= 4*mid):
            low = mid + 1
        else:
            high = mid - 1
    return low + (high-low)//2

t  = int(input())
for i in range(t):
    a,b = list(map(int,input().split()))
    print( binarySearch(a,b))