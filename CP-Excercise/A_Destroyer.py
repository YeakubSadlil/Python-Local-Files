tst = int(input())

for _ in range(tst):
    n = int(input())
    reported_numbers = list(map(int, input().split()))

    mx = 0
    tmp = 0
    count = [0] * 101

    for x in reported_numbers:
        mx = max(mx, x)
        count[x] += 1

    for i in range(1, mx + 1):
        if count[i - 1] < count[i]:
            print("NO")
            tmp = 1
            break

    if tmp == 0:
        print("YES")
