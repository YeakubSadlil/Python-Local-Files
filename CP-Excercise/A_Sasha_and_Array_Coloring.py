tst = int(input())

for _ in range(tst):

    n = int(input())
    lst = list(map(int, input().split()))
    lst.sort()
    sum = 0
    tmp = n
    for i in range(n//2):
        sum += lst[tmp-1] - lst[i]
        tmp -= 1
    print(sum)