for _ in range(t):
    s = input()

    result = '0'
    s_list = list(s) 
    for i in range(0, len(s_list)):
        if s_list[i] == '?':
            s_list[i] = result
        else:
            result = s_list[i]
    result_str = "".join(s_list)
    print(result_str)
